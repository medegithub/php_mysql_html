SET NAMES UTF8;

DROP DATABASE IF EXISTS userinfo;
CREATE DATABASE userinfo CHARSET=UTF8;

USE userinfo;

CREATE TABLE user_msg(
	uid INT PRIMARY KEY AUTO_INCREMENT,
	uname VARCHAR(20),
	upsw VARCHAR(20),
	uweb VARCHAR(300),
	uage VARCHAR(10),
	ubirthday VARCHAR(32)
);

CREATE TABLE goods(
	gid INT PRIMARY KEY AUTO_INCREMENT,
	uid INT,
	gname VARCHAR(500),
	gpic VARCHAR(100),
	gprice FLOAT(10,2)
);






